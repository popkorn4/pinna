// Типизированный список событий realtime.
// Имена короткие, контракт минимальный — только сигнал что что-то изменилось.
// Клиент по событию делает router.refresh() и подтягивает свежий снапшот.
// почему так: меньше места для гонок и проще отлаживать. Цена — лишний RTT
// до сервера, но это приемлемо для board-ивентов раз в секунду.

export type RealtimeEvent =
  | { type: "board:changed" }
  | { type: "card:opened"; cardId: string };

export const REALTIME_EVENT = "rt"; // одно имя события на канал
